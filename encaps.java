package Encapsulation;

public class encaps {
	
	private String name;
	private int rollNo;
	
	// Data Set Method for Name
	
	public void setName(String newName){
		name = newName;
	}
	
	// Data Set Method for rollNo
	
	public void setrollNo(int newrollNo){
		 rollNo = newrollNo;
	}
	
	// Data get method for Name
	public String getName(){
		return name;
	}
	
	public int getrollNo(){
		return rollNo;
	}


}

